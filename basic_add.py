def basic_add(num1, num2):
    return num1 + num2

def basic_sub(num1, num2):
    return num1 - num2

def basic_mul(num1, num2):
    return num1 * num2

addRes = basic_add(10, 20)
subRes = basic_sub(10,20)
mulRes = basic_mul(10,20)

print('addtion result:', addRes)
print('subtraction: ', subRes)
print('multiplication:', mulRes)

